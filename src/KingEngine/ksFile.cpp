int ksFile::readInt()
{
	int temp = -1;

	if (is_open)
	{
		//sf::String temp_string = m_currentFile.substr(m_currentIndex, 2);
		//sf::String temp_string = m_currentFile.
		char * temp_char = new char[3];
		temp_char[0] = m_currentFile[m_currentIndex];
		temp_char[1] = m_currentFile[m_currentIndex + 1];
		temp_char[2] = '\0';

		temp = atoi(temp_char);

		std::stringstream temp_stream;
		temp_stream << temp;
		std::string temp_str = temp_stream.str();

		if (m_currentIndex < 10)
			m_debugText->setString(m_debugText->getString() + "ksFile reading integer: " + temp_str + "\n");

		m_currentIndex += 3;

		if (m_currentIndex >= (m_currentFile.getSize() - 2))
			is_open = false;

		delete[] temp_char;
	}

	return temp;
}

